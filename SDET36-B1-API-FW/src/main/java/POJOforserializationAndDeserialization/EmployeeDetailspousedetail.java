package POJOforserializationAndDeserialization;

public class EmployeeDetailspousedetail {
	
	
	String name;
	int phno;
	String company;
	
	public EmployeeDetailspousedetail() {
		
	}

	public EmployeeDetailspousedetail(String name, int phno, String company) {
		this.name = name;
		this.phno = phno;
		this.company = company;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPhno() {
		return phno;
	}

	public void setPhno(int phno) {
		this.phno = phno;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}
	
	
	

}
